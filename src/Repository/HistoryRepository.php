    public function getMonthsList()
    {
        $qb = $this->createQueryBuilder('h')
            ->select(['h.year', 'h.month'])
            ->groupBy('h.year')
            ->addGroupBy('h.month')
            ->orderBy('h.year', 'DESC')
            ->addOrderBy('h.month', 'DESC');

        return $qb->getQuery()->execute();
    }

